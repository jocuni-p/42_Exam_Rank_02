#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac != 2)
		return (write(1, "\n", 1));
	while (av[1][i] != '\0')
	{
		if (av[1][0] >= 65 && av[1][0] <= 90)
		{
			av[1][i] = av[1][i] + 32;
			write(1, &av[1][i], 1);
		}
		else if (av[1][i] == 95 && (av[1][i + 1] >= 97 && av[1][i + 1] <= 122))
		{
			i++;
			av[1][i] = av[1][i] - 32;
			write(1, &av[1][i], 1);
		}
		else
			write(1, &av[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
